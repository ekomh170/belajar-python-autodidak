# operasi komperasi

# setiap hasil dari operasi adalah boolean

# >, <, >=, <=, ==, !=, is, is not

a = 4
b = 2

# lebih besar dari = >
hasil = a > b
print("\nLebih Besar Dari = >")
print(a, '>', b, '=', hasil)
hasil = b > 3
print(b, '>', 3, '=', hasil)
hasil = b > 2
print(b, '>', 2, '=', hasil)

# kurang besar dari = <
print("\nKurang Besar Dari = <")
hasil = a < b
print(a, '<', b, '=', hasil)
hasil = b < 3
print(b, '<', 3, '=', hasil)
hasil = b < 2
print(b, '<', 2, '=', hasil)

# Lebih besar dari sama dengan = >=
print("\nLebih Besar Dari Sama Dengan = >=")
hasil = a >= b
print(a, '>=', b, '=', hasil)
hasil = b >= 3
print(b, '>=', 3, '=', hasil)
hasil = b >= 2
print(b, '>=', 2, '=', hasil)

# kurang dari sama dengan = <=
print("\nKurang Besar Dari Sama Dengan = <=")
hasil = a <= b
print(a, '<=', b, '=', hasil)
hasil = b <= 3
print(b, '<=', 3, '=', hasil)
hasil = b <= 2
print(b, '<=', 2, '=', hasil)

# sama dengan =  ==
print("\nSama Dengan = ==")
hasil = a == b
print(a, '==', b, '=', hasil)
hasil = b == 3
print(b, '==', 3, '=', hasil)
hasil = b == 2
print(b, '==', 2, '=', hasil)

# tidak sama dengan =  !=
print("\nTidak Sama Dengan = !=")
hasil = a != b
print(a, '!=', b, '=', hasil)
hasil = b != 3
print(b, '!=', 3, '=', hasil)
hasil = b != 2
print(b, '!=', 2, '=', hasil)
